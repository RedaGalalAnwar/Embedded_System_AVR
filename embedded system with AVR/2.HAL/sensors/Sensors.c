u16 VoltRead_InMilli_10Bits(ADC_Channel channel)
{
	u16 adc=ADC_AnalogRead(channel);
		u16 milli_Volt=((u32)5000*adc)/1024;
		return milli_Volt;				
}




u16 TEMP_Read_10Bits(ADC_Channel channel)
{
	u16 adc=ADC_AnalogRead(channel);
	u16 volt=((u32)5000*adc)/1024;
	u16 temp=volt/10;
	return temp;
}